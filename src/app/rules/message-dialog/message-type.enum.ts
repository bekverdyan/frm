/**
 * Message Box dialog type. This enumeration is being used to show Message Box dialog
 * with appropriate design and styling. For example if the Message Box type is 'ERROR',
 * it will be shown with the appropriate icon and red-colored text.
 *
 * @author Karen Margaryan
 * @since 1.0.0
 */
export enum MessageType {
    Info = 'INFO',
    Success = 'SUCCESS',
    Warning = 'WARNING',
    Error = 'ERROR'
}
